package com.ericsson.cifwk.scanner.model;

public class TestStep extends PublicMethod {

    private String id;

    private String description;

    public TestStep(String name, String className) {
        super(name, className);
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return String.format("%s %s from %s (%s)", getReturnType(), getName(), getClassName(), id);
    }
}
